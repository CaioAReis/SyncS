export const regexValidations = {

  CEP: /^[0-9]{5}-[\d]{3}/,

  CPF: /^\d{3}\.\d{3}\.\d{3}-\d{2}$/,

  PHONE: /^\([0-9]{2}\) [0-9]{5}-[0-9]{4}$/,

  NAME: /^[A-Z][a-zA-Zà-úÀ-Ú\s]+(?:[ ][a-zA-Zà-úÀ-Ú\s]+)*$/,

  EMAIL: RegExp(/^[a-zA-Z0-9]+(?:[._-][a-zA-Z0-9]+)*@[a-zA-Z0-9]+(?:\.[a-zA-Z0-9]+)*$/),

  DATE: /^(((0[1-9]|[12][0-9]|30)[-/]?(0[13-9]|1[012])|31[-/]?(0[13578]|1[02])|(0[1-9]|1[0-9]|2[0-8])[-/]?02)[-/]?[0-9]{4}|29[-/]?02[-/]?([0-9]{2}(([2468][048]|[02468][48])|[13579][26])|([13579][26]|[02468][048]|0[0-9]|1[0-6])00))$/,

};

export const masks = {

  phone: (e: string) => e
    .replace(/\D/g, "")
    .replace(/(\d{2})(\d{1})/, "($1) $2")
    .replace(/(\d{5})(\d{1})/, "$1-$2")
    .replace(/(\d{4})/, "$1"),

  date: (e: string) => e
    .replace(/\D/g, "")
    .replace(/(\d{2})(\d)/, "$1/$2")
    .replace(/(\d{2})(\d)/, "$1/$2")
    .replace(/(\d{4})(\d{1,2})/, "$1/$2"),

};